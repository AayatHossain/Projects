/** Returns the courses localized for the given language (English structure preserved). */
export function coursesFor(lang: 'en' | 'bn'): Course[] {
  if (lang !== 'bn') return COURSES;
  return COURSES.map((c) => {
    const ct = BN[c.key];
    if (!ct) return c;
    return {
      ...c,
      title: ct.title ?? c.title,
      blurb: ct.blurb ?? c.blurb,
      lectures: c.lectures.map((lec, i) => {
        const lt = ct.lectures?.[i];
        if (!lt) return lec;
        return {
          title: lt.title ?? lec.title,
          body: lt.body ?? lec.body,
          quiz: lec.quiz.map((q, j) => {
            const qt = lt.quiz?.[j];
            if (!qt) return q;
            return {
              q: qt.q ?? q.q,
              options: qt.options ?? q.options,
              correct: q.correct,
              why: qt.why ?? q.why,
            };
          }),
        };
      }),
    };
  });
}
